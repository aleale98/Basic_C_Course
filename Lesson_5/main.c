#include <stdio.h>
#include <stdlib.h>

int main() {
  /*A variable is a memory location
    that can store only ONE VALUE
  */
  //declaration
  int a;
  float b;
  char c;
  //initialization
  a = 7;
  b = 7.9;
  c = 'x';
  printf("Hello World!\n");
  printf("%d\n",a);
  printf("%f\n",b);
  printf("%c\n",c);
  return 0;
}
